#!/usr/bin/env python3

number = int(input('숫자를 입력하세요: '))

if number == 1:
        spanish = 'uno'
elif number == 2:
        spanish = 'dos'
elif number == 3:
        spanish = 'tres'
elif number == 4:
        spanish = 'cuatro'
elif number == 5:
        spanish = 'cinco'
else:
        spanish = ''

print(spanish)
